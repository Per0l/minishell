#include "minishell.h"

int	builtin_env(void)
{
	int	i;

	i = 0;
	while (__environ[i])
	{
		write(1, __environ[i], ft_strlen(__environ[i]));
		write(1, "\n", 1);
		i++;
	}	
	return (1);
}
